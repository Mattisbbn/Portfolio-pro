    <h2 class="text-center mt-10 md:text-4xl text-2xl font-semibold mb-6">Contexte</h2>
    <p class="text-lg text-gray-600 font-medium">Ce projet a été réalisé dans le cadre de ma formation en BTS SIO (Services Informatiques aux Organisations), option SLAM (Solutions Logicielles et Applications Métiers). L’objectif pédagogique était de mettre en pratique nos compétences en développement web avec Laravel, tout en travaillant en conditions proches d’un environnement professionnel.

    <br><br>
    Ce projet de groupe avait pour but de concevoir une application web de type “Click & Collect”, appliquée ici au domaine de la restauration, sous le nom fictif de Click&Eat. L’application devait permettre aux clients de réserver une table dans un restaurant, tout en ayant la possibilité de passer leur commande à l’avance, afin de réduire le temps d’attente une fois sur place.</p>
    <br>

   
<br>
    <p class="text-lg text-gray-600 font-medium">Ce projet m’a permis de travailler dans un contexte professionnel réel, en respectant un cahier des charges client, des délais, et en échangeant régulièrement avec le client et les membres de l’équipe d’Akoufen.</p>
   

    <h2 class="text-center mt-10 md:text-4xl text-2xl font-semibold mb-6">Objectifs</h2>

   <p class="text-lg text-gray-600 font-medium">Proposer une solution optimisée pour plusieurs restaurants qui proposent des menus différents.

Optimiser les échanges entre les différents acteurs du restaurant (administration, service, cuisine, clients).</p>


